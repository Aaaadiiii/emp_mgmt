    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         
        int id,sal;
       
        id = Integer.parseInt(eid.getText());       
        String url ="jdbc:mysql://localhost:3306/project";
       String driver = "com.mysql.cj.jdbc.Driver";
       String user = "root";
       String password="aadi";
       Connection con;
       Statement st;
       ResultSet rs;
       try{
           Class.forName(driver);
           con= DriverManager.getConnection(url,user,password);
           String query = "select * from emp where eid = "+id+"";
           st = con.createStatement();
           rs = st.executeQuery(query);
           if(rs.next()){
               
               JOptionPane.showMessageDialog(this, "ID found");
               sal = rs.getInt("esal");
               ename.setText(rs.getString("ename"));
               epwd.setText(rs.getString("epwd"));
               esal.setText(String.valueOf(sal));
           }
           else{
              
               JOptionPane.showMessageDialog(this, "ID not found");
           }
       }
       catch(Exception ex){
           System.out.println(ex.getMessage());
           
       }
    }//GEN-LAST:event_jButton3ActionPerformed

